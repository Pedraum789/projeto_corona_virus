    public void desenhaMundo(){
        for(int i=0; i < mapa.length; i++){
            for(int j=0; j < mapa[i].length; j++){
                switch(mapa[i][j]){
                    case 0:
                        System.out.print(" ");
                        break;
                    case 1:
                        System.out.print("\033[47m \033[0m");
                        break;
                    case 2:
                        System.out.print("\033[45m \033[0m");
                        break;
                    case 3:
                        System.out.print("\033[44m \033[0m");
                        break;
                }
            }
            System.out.println();
        }
        System.out.println();
        System.out.println();
    }
